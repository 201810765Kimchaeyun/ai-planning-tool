        for slide_num, text in slide_texts:
            if text.strip():
                st.markdown(f"**Slide {slide_num}**")
                st.markdown(
                    '<div class="slide-card">' + text.replace("\n", "<br>") + '</div>',
                    unsafe_allow_html=True,
                )

