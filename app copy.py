# HTML report helper
# ─────────────────────────────────────────────
def build_html_report(ux_issues, policy_gaps, edge_cases, test_cases):
    ux_df = pd.DataFrame([
        {
            "Title": item.get("title", ""),
            "Description": item.get("description", ""),
            "Impact": item.get("impact", ""),
        }
        for item in ux_issues
    ])

    policy_df = pd.DataFrame([
        {
            "Title": item.get("title", ""),
            "Description": item.get("description", ""),
            "Recommendation": item.get("recommendation", ""),
        }
        for item in policy_gaps
    ])

    edge_df = pd.DataFrame([
        {
            "Title": item.get("title", ""),
            "Scenario": item.get("scenario", ""),
            "Expected Behavior": item.get("expected_behavior", ""),
        }
        for item in edge_cases
    ])

    test_case_df = pd.DataFrame([
        {
            "TC #": t.get("id", ""),
            "Test Case": t.get("test_case", ""),
            "Description": t.get("description", ""),
            "Expected Result": t.get("expected_result", ""),
            "Priority": t.get("priority", ""),
        }
        for t in test_cases
    ])

    def table_or_empty(df, empty_text="결과 없음"):
        if df.empty:
            return f"<p>{empty_text}</p>"
        return df.to_html(index=False, escape=False)

    return f"""
    <!DOCTYPE html>
    <html lang="ko">
    <head>
        <meta charset="UTF-8">
        <title>AI 기획 리뷰 결과</title>
        <style>
            body {{
                font-family: Arial, sans-serif;
                margin: 32px;
                color: #1e293b;
                line-height: 1.6;
            }}
            h1 {{
                margin-bottom: 8px;
            }}
            h2 {{
                margin-top: 32px;
                border-bottom: 2px solid #e2e8f0;
                padding-bottom: 6px;
            }}
            table {{
                width: 100%;
                border-collapse: collapse;
                margin-top: 12px;
                margin-bottom: 28px;
                table-layout: fixed;
                word-wrap: break-word;
            }}
            th, td {{
                border: 1px solid #cbd5e1;
                padding: 10px;
                text-align: left;
                vertical-align: top;
                font-size: 14px;
            }}
            th {{
                background: #f1f5f9;
            }}
            .meta {{
                color: #64748b;
                margin-bottom: 24px;
            }}
        </style>
    </head>
    <body>
        <h1>AI 기획 리뷰 결과</h1>
        <div class="meta">
            <div>Generated by AI Planning Review Tool</div>
        </div>

        <h2>1. UX Issues</h2>
        {table_or_empty(ux_df)}

        <h2>2. Policy Gaps</h2>
        {table_or_empty(policy_df)}

        <h2>3. Edge Cases</h2>
        {table_or_empty(edge_df)}

        <h2>4. QA Test Cases</h2>
        {table_or_empty(test_case_df)}
    </body>
    </html>
    """

