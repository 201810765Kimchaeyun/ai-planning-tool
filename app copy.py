# ─────────────────────────────────────────────
# Leave warning
# ─────────────────────────────────────────────
def inject_leave_warning(enabled: bool):
    script = f"""
    <script>
    const warningEnabled = {str(enabled).lower()};
    window.onbeforeunload = null;

    if (warningEnabled) {{
        window.onbeforeunload = function (e) {{
            e.preventDefault();
            e.returnValue = '';
            return '';
        }};
    }}
    </script>
    """
    components.html(script, height=0)

